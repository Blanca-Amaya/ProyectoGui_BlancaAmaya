    private void Boton_Ok_InputDialogMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Boton_Ok_InputDialogMouseClicked

        String nombre = Texto_Alumno.getText();
        Random random = new Random();
        String casa = "";
        int casaAlt = random.nextInt(4);
        switch (casaAlt) {
            case 0 -> {
                casa = "Slytherin";
                estudiantesSlytherin.add(new Estudiante(nombre, casa));
                contador4++;
                mt1.addRow(new Object[]{nombre, casa});
                break;
            }
            case 1 -> {
                casa = "Gryffinder";
                estudiantesGryffinder.add(new Estudiante(nombre, casa));
                contador1++;
                mt4.addRow(new Object[]{nombre, casa});
                break;
            }
            case 2 -> {
                casa = "Hufflepuff";
                estudiantesHufflepuff.add(new Estudiante(nombre, casa));
                contador2++;
                mt2.addRow(new Object[]{nombre, casa});
                break;
            }
            case 3 -> {
                casa = "RavenClaw";
                estudiantesRavenClaw.add(new Estudiante(nombre, casa));
                contador3++;
                mt3.addRow(new Object[]{nombre, casa});
                break;
            }
        }

        mt.addRow(new Object[]{nombre, casa});
        Texto_Alumno.setText("");

        Input_JDialog.dispose();
        this.setVisible(true);
        if (mt1.getRowCount() > 0 || mt2.getRowCount() > 0 || mt3.getRowCount() > 0 || mt4.getRowCount() > 0) {
            String casaAsignada = "";
            if (mt1.getRowCount() > 0) {
                int finFila = mt1.getRowCount() - 1;
                casaAsignada = mt1.getValueAt(finFila, 1).toString();
            } else if (mt2.getRowCount() > 0) {
                int finFila = mt2.getRowCount() - 1;
                casaAsignada = mt2.getValueAt(finFila, 1).toString();
            } else if (mt3.getRowCount() > 0) {
                int finFila = mt3.getRowCount() - 1;
                casaAsignada = mt3.getValueAt(finFila, 1).toString();
            } else if (mt4.getRowCount() > 0) {
                int finFila = mt4.getRowCount() - 1;
                casaAsignada = mt4.getValueAt(finFila, 1).toString();
            }
            Label_2.setText(casaAsignada);
            this.setVisible(false);
            expandir(Mensaje_JDialog);
        } else {
            Label_2.setText("No hay registro de estudiantes.");
        }
        actualizarContadores();
    }//GEN-LAST:event_Boton_Ok_InputDialogMouseClicked

